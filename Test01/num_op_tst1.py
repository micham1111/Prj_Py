# Casting allows you to convert from one type to another
# float to int
print(f" type(int(5.4)) =  { type(int(5.4)) }" )      # type(int(5.4)) =  <class 'int'>

# float to string
print(f" type(str(5.4)) = { type(str(5.4)) }")       # type(str(5.4)) = <class 'str'>

# integer to float
print(f" type(float(2)) = { type(float(2)) } ")      # type(float(2)) = <class 'float'> 

# unicode character to string
print(f" type(chr(97)) = { type(chr(97)) } ")        # type(chr(97)) = <class 'str'>

# character to unicode
print(f" type(ord('a')) = { type(ord('a')) } ")     # type(ord('a')) = <class 'int'>

